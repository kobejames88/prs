package com.perfectchina.bns.model;

public class TreeNodeResult {
	
	private String pin;
	private Float opv;
	private Float gpv;
	
	
	public String getPin() {
		return pin;
	}
	public void setPin(String pin) {
		this.pin = pin;
	}
	public Float getOpv() {
		return opv;
	}
	public void setOpv(Float opv) {
		this.opv = opv;
	}
	public Float getGpv() {
		return gpv;
	}
	public void setGpv(Float gpv) {
		this.gpv = gpv;
	}
	
}
